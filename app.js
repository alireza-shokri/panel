// clear_class
function clear_special(vrodi){
  vrodi.forEach(function(elm){
       if(elm.classList.contains('special'))
            elm.classList.remove('special')
    })
}

// disabled false
function disabled_false(vrodi2){
    vrodi2.forEach(function(elm){
        elm.disabled=true;
})}

// disabled true
function disabled_true(vrodi2){
    vrodi2.forEach(function(elm){
        elm.disabled=false;
})}


// class_is
function class_is(vrodi3){
    for(var i=0;i<vrodi3.length;i++){
        if(vrodi3[i].classList.contains('special'))
            return true;
    }
    return false;
}

function btns_hed(event){
     
    if(event.target.id == 'kind1' && event.target !== which){
        disabled_true(btns2)
        disabled_true(btns3)
        clear_special(btns)
        event.target.classList.add('special')

        if(!class_is(btns2)) btns2[0].classList.add('special')

        if(!class_is(btns3)) btns3[0].classList.add('special')

        
    
    }

    if(event.target.id == 'kind2' && event.target !== which){
        disabled_false(btns2)
        clear_special(btns2)
        disabled_true(btns3)
        clear_special(btns)
        event.target.classList.add('special')

        if(!class_is(btns3)) btns3[0].classList.add('special')
    }

    if(event.target.id == 'kind3' && event.target !== which){
        disabled_false(btns2)
        disabled_false(btns3)
        clear_special(btns2)
        clear_special(btns3)
        clear_special(btns)
        event.target.classList.add('special')
    }
    
    which=event.target;
    write_table()
}


function btns2_hed(event){
    clear_special(btns2)
    event.target.classList.add('special')
    write_table()
}
function btns3_hed(event){
    clear_special(btns3)
    event.target.classList.add('special')
    write_table()
}

// do write
function do_write(which_list){
    for(var i=0;i<liTable.length;i++){
        liTable[i].innerHTML=(which_list[i])
    }
}

// select list
